# operasi aritmatika
# * / + - , **(pangkat),
# % modulus adalah sisa pembagian..
# // floor division, kebalikan modulus
a = 10
b = 3

print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)
print("a ** b = ", a ** b)
print("a % b = ", a % b)
print("a // b = ", a // b)
# maksudnya, 10 / 3 buletnya jadi berapa ya, hasilnya 3
# nah, sisanya barulah itu modulus,
# itu maksudnya modulus kebalikan floor division

